const name = "haga, pada, susu, susu";

// indexOf()
console.log(name.indexOf("pada"));

// lastIndexOf()
console.log(name.lastIndexOf("susu"));

// includes()
console.log(name.includes("susu"));

// startsWith()
console.log(name.startsWith("susu"));

// endsWith()
console.log(name.endsWith("susu"));

// getting substring using slice(startIndex, endIndex+1)
console.log(name.slice(0, 4));
